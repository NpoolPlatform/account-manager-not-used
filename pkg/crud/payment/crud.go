func CreateSet(c *ent.PaymentCreate, in *npool.AccountReq) *ent.PaymentCreate {
	if in.ID != nil {
		c.SetID(uuid.MustParse(in.GetID()))
	}
	if in.AccountID != nil {
		c.SetAccountID(uuid.MustParse(in.GetAccountID()))
	}
	if in.CollectingTID != nil {
		c.SetCollectingTid(uuid.MustParse(in.GetCollectingTID()))
	}
	if in.AvailableAt != nil {
		c.SetAvailableAt(in.GetAvailableAt())
	}
	return c
}

func Create(ctx context.Context, in *npool.AccountReq) (*ent.Payment, error) {
	var info *ent.Payment
	var err error

	_, span := otel.Tracer(constant.ServiceName).Start(ctx, "Create")
	defer span.End()

	defer func() {
		if err != nil {
			span.SetStatus(codes.Error, "db operation fail")
			span.RecordError(err)
		}
	}()

	span = tracer.Trace(span, in)

	err = db.WithClient(ctx, func(_ctx context.Context, cli *ent.Client) error {
		c := cli.Payment.Create()
		info, err = CreateSet(c, in).Save(_ctx)
		return err
	})
	if err != nil {
		return nil, err
	}

	return info, nil
}

func CreateBulk(ctx context.Context, in []*npool.AccountReq) ([]*ent.Payment, error) {
	var err error

	_, span := otel.Tracer(constant.ServiceName).Start(ctx, "CreateBulk")
	defer span.End()

	defer func() {
		if err != nil {
			span.SetStatus(codes.Error, "db operation fail")
			span.RecordError(err)
		}
	}()

	span = tracer.TraceMany(span, in)

	rows := []*ent.Payment{}
	err = db.WithTx(ctx, func(_ctx context.Context, tx *ent.Tx) error {
		bulk := make([]*ent.PaymentCreate, len(in))
		for i, info := range in {
			bulk[i] = CreateSet(tx.Payment.Create(), info)
		}
		rows, err = tx.Payment.CreateBulk(bulk...).Save(_ctx)
		return err
	})
	if err != nil {
		return nil, err
	}
	return rows, nil
}

